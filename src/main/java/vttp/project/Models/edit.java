package vttp.project.Models;

public class edit {

    private int age;
    private int height;
    private int weight;

    public edit(int age, int height, int weight) {
        this.age = age;
        this.height = height;
        this.weight = weight;
    }

    public int getAge() {
        return age;
    }

    public int getHeight() {
        return height;
    }

    public int getWeight() {
        return weight;
    }

    
    
}
